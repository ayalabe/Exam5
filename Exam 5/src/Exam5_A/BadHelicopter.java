package Exam5_A;
/**
 * SquarePegs are not compatible with RoundHoles (they were implemented by
 * previous development team). But we have to integrate them into our program.
 */
public class BadHelicopter {
    
	public void yawsRight() {
		System.out.println("Helicopter is yaws Right");
	}
	
	public void yawsLeft() {
		System.out.println("Helicopter is yaws Left");
	}
	
	public void fliesUp() {
		System.out.println("Helicopter is flies Up");
	}
	
	public void fliesDown() {
		System.out.println("Helicopter is flies Down");
	}
	
	public void launchesMissile() {
		System.out.println("Helicopter is launches a Missile");
	}
}